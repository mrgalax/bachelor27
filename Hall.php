            <h1>Under 1864 krigen</h1>

            <p>Den tyske invasion i Slesvig begyndte 31. januar 1864. Monrad beordrede, at Dannevirke ikke måtte opgives, før tredjedelen af den danske styrke var tabt. Da Dannevirke alligevel måtte evakueres, bad han om at alle måtte kæmpe, til København var faldet. På statsrådsmødet den 26. februar støttede Monrad krigsminister Carl Lundbyes krav om at afsætte den øverstkommanderende, general de Meza. Kongen, Christian 9., argumenterede kraftigt for at beholde generalen på posten, men da Lundbye satte sin stilling på spil og Monrad bakkede sin minister op, bøjede kongen sig for kravet. [4] Mens de danske tropper forskansede sig ved Dybbøl og på Als, blev Slesvig og Jylland besat af tyske tropper. Få dage senere erklærede den nye øverstkommanderende, at han heller ikke kunne holde Dybbøl. Monrad beordrede nu, at Dybbøl skulle holdes. 18. april 1864 stormede prøjsiske tropper skanserne. Totalt omkom ca. 3000 danske soldater i krigen, og over 3.500 blev taget til fange. Der er dog store forskelle mellem de officielle preussiske og tilsvarende danske tal. Forskere har forklaret disse uoverensstemmelser med, at begge parter underdrev deres egne tab og overdrev fjendens. I forbindelse med markeringen af 150-året for slaget ved Dybbøl blev de danske tab her opført som 1669 døde og sårede samt 3131 fanger og desertører.[5] Krigen blev afløst af våbenhvile.

