            <h1>Carl Christian Hall</h1>

            <p>Carl Christian Hall (25. februar 1812 på Christianshavn – 14. august 1888 på Frederiksberg) var en dansk nationalliberal politiker, minister og konseilspræsident, Danmarks 6. og 8. statsminister i to perioder, første gang i perioden 13. maj 1857 – 2. december 1859 og anden gang fra den 24. februar 1860 til 31. december 1863. Han stod desuden i spidsen for mange andre ministerier. Han var en betydningsfuld skikkelse i store dele af 1800-tallets danske politik og han var medansvarlig for overgangen fra en politik, der vægtede helstaten, til en politik, der vægtede konstitutionelt demokrati, det nationale tilhørsforhold og Ejderpolitikken. Sammen med D.G. Monrad dannede han lederskabet hos De Nationalliberale, og deres politik dannede optakten til den 2. slesvigske krig. Hall var en udpræget pragmatisk forhandlingspolitiker, var folkekær og vellidt også af politiske modstandere, og hans popularitet forblev høj på trods af den katastrofale udgang på krigen i 1864.</p>
