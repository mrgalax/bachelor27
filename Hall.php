            <h1>Professionelle liv</h1>

            <p>Men da terningerne var kastede, sluttede Hall sig til den kreds, han naturligt tilhørte, og blev en fuldblods repræsentant for den nationalliberale politik. Karakteristisk nok ser vi dog hverken Hall som folketaler eller som forfatter af avisartikler; det var som parlamentariker, han trådte ind på arenaen, støttet af A.W. Moltke og Knuth, da Martsministeriet udnævnte ham til kongevalgt medlem af den Roskilde stænderforsamling, som skulle drøfte valgloven til Den grundlovgivende Rigsforsamling. Han ansås af sine politiske venner almindeligvis for at stå noget til højre for dem og gjorde det sikkert også, men i hovedprincipperne: Ejderpolitik og konstitutionel forfatning var han enig med dem, og han var, lige fra begyndelsen tilstrækkelig opportunist til at kunne lempe sine særanskuelser efter øjeblikkets krav. Han lagde i stænderne ingen skjul på, at han kun med ængstelse så den konstituerende forsamling så demokratisk sammensat som efter valglovsudkastet, men dog tiltrådte han dette med den ofte citerede motivering, at vi nys havde gennemlevet en tid, hvor "Dage havde Aartiers Betydning og Indhold". Selv valgtes han ind i rigsforsamlingen som repræsentant for Frederiksbergkredsen (Københavns Amts 1. kreds), den han siden uafbrudt repræsenterede som Folketingsmand indtil 1881. Den eneste seriøse udfordrer i Halls valgkreds var helstatsmanden, professor Peder Hjort, der måtte se sig slået i 1853. Hall kom snart til at spille en betydelig rolle i rigsforsamlingen og blev medlem af grundlovsudvalget og dettes ordfører for afsnittet om borgernes personlige rettigheder. Ikke uden grund har han en fremtrædende placering i Constantin Hansens maleri af rigsforsamlingen..</p>
