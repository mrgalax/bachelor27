            <h1>Personlige liv</h1>

            <p>C.C. Hall blev født på Christianshavn som søn af bødkermester, oberstløjtnant i Borgervæbningen Mads Christensen Hall, der stammede fra jyske bønderfolk i Randersegnen, og dennes anden hustru, Anna Helene født Lykke. I sin barndom var han svagelig og sen i sin udvikling, så at han var fyldt syv år, førend han kunne begynde nogen undervisning, men fra den tid styrkedes hans helbred, og med legemets kraft voksede også de åndelige evner.

                Hall blev student fra Borgerdydskolen på Christianshavn 1829, hvor han havde været en del af en talentfuld klasse, der omfattede C.E. Fenger, Victor Bloch, lægen Andreas Buntzen og Vilhelm Birkedal, og blandt Halls studenterkammerater var desuden D.G. Monrad, grev F.M. Knuth, brødrene Martin og Frederik Hammerich, Georg Aagaard og Frederik Barfod. Han blev juridisk kandidat 1833 og gjorde året efter sammen med Knuth en længere rejse til Tyskland, Frankrig og Italien. Efter sin hjemkomst blev han en meget søgt juridisk manuduktør. 1837 udnævntes han til auditør i Hæren (chef for Hærens retsvæsen), 1840 tog han den juridiske licentiatgrad, 1844 blev han overauditør og 1847 ekstraordinær docent i romerret ved Universitetet. 1851 blev han titulær professor. Han havde ved sit ægteskab (1. december 1837) med filologen Peter Oluf Brøndsteds datter, Augusta Marie Frederikke Brøndsted (f. 24. juni 1816), fået gode sociale forbindelser og var personlig velset af kong Christian VIII. Kongen viste sit nådige sindelag ved at tilbyde Hall kammerjunkertitlen og ved ikke at tage hans afslag ilde op.

                Allerede 1840 havde han, da lægen dr. Seligman Meyer Trier havde erklæret et landophold for nødvendigt til hustruens helbredelse fra en længere svækkelse, købt Ny Bakkegård i Rahbeks Allé på Frederiksberg, som i så lang en årrække skulle blive hans hjem, og som grundejer var han blevet valgt ind i Frederiksberg-Hvidovre Sogneforstanderskab (1841-54).

                Forbindelsen til kongehuset samt Halls ry som fin jurist, hans smukke og repræsentative personlighed og hans almindeligt vindende væsen gjorde, at han overalt blev betragtet som en af de mest lovende individer i sin generation. Dog var det snarere en glimrende embedskarriere end politiske laurbær, man spåede ham, for en vis skyhed for det agitatorisk fremtrædende i forbindelse med personlige hensyn havde holdt ham tilbage fra at deltage i ungdomsvennernes oppositionelle bestræbelser i 1840'erne og under selve martsbevægelsen, der arbejdede for en fri forfatning. Han deltog hverken i Casinomødet eller i folketoget 22. marts 1848 og var i begyndelsen skeptisk over for de nye strømninger</p>

