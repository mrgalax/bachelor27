                <h1>Under 1864 krigen</h1>

                <p>Under den 2. Slesvigske Krig var Monrad konseilspræsident 31.december 1863 – 11. juli 1864, efter at Statsminister C.C. Hall , som var tilhænger af en løsning med indrømmelser til tyskerne, havde søgt sin afsked. Monrad havde udtalt, at han selv foretrak krig frem for, at det Tyske Forbund skulle gribe ind i dansk suverænitet.

                    Ved siden af posten som ledende minister varetog Monrad hvervene som finansminister og minister for Holsten og Lauenborg. En tid var han også vikar for udenrigsministeren. De fleste mener, at han overvurderede sine evner og undervurderede den farlige politiske situation.[3] Han blev opfattet som kritikløs og dominerende, og han mødte skepsis og mistillid blandt andre politikere.

                    Den tyske invasion i Slesvig begyndte 31. januar 1864. Monrad beordrede, at Dannevirke ikke måtte opgives, før tredjedelen af den danske styrke var tabt. Da Dannevirke alligevel måtte evakueres, bad han om at alle måtte kæmpe, til København var faldet. På statsrådsmødet den 26. februar støttede Monrad krigsminister Carl Lundbyes krav om at afsætte den øverstkommanderende, general de Meza. Kongen, Christian 9., argumenterede kraftigt for at beholde generalen på posten, men da Lundbye satte sin stilling på spil og Monrad bakkede sin minister op, bøjede kongen sig for kravet. [4] Mens de danske tropper forskansede sig ved Dybbøl og på Als, blev Slesvig og Jylland besat af tyske tropper. Få dage senere erklærede den nye øverstkommanderende, at han heller ikke kunne holde Dybbøl. Monrad beordrede nu, at Dybbøl skulle holdes. 18. april 1864 stormede prøjsiske tropper skanserne. Totalt omkom ca. 3000 danske soldater i krigen, og over 3.500 blev taget til fange. Der er dog store forskelle mellem de officielle preussiske og tilsvarende danske tal. Forskere har forklaret disse uoverensstemmelser med, at begge parter underdrev deres egne tab og overdrev fjendens. I forbindelse med markeringen af 150-året for slaget ved Dybbøl blev de danske tab her opført som 1669 døde og sårede samt 3131 fanger og desertører.[5] Krigen blev afløst af våbenhvile.

                    Monrad lod sig ikke anfægte af dette nederlag. Han ville nu etablere en nordisk union, og skrev til den svenske konge om at arrangere et kongeligt giftermål, som skulle forene de to lande. Kongen vidste ikke, om brevet var skrevet i spøg eller alvor. Under London-konferencen blev der først foreslået en deling af Slesvig, siden en voldgiftsret til løsning af det slesvigske spørgsmål. Monrad afslog først, men kom i vildrede og overlod den endelige afgørelse til kongen, som afslog kompromisforslagene. Krigen brød derfor ud igen 25.juni 1864. Als blev erobret af preusserne. Monrad forlangte nu, at krigen skulle fortsætte, indtil den nåede Københavns gader, hvor man kunne kæmpe mand mod mand.[6] Han blev afsat som statsminister af kongen 8. juli. Monrad græd og skreg: "Kongen har forrådt mig!" Indenrigsministeren måtte tage sig af ham og erklærede, at Monrad var fra forstanden.[6]

                    </p>

