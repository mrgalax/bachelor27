                    <h1>Professionelle liv</h1>

                    <p>I 1840 blev Monrad politisk redaktør af Fædrelandet, og fra 1843 redigerede han Selskabet for Trykkefrihedens rette Brugs medlemsblad Dansk Folkeblad.

                        Som folkevalgt sad Monrad i Københavns Borgerrepræsentation i 1842-1848. Han sad i Folketinget i 1850-1853, 1853-1865 og 1882-1886.

                        I 1846-1871 var Monrad sognepræst i flere omgange, bl.a. i Vester Ulslev på Lolland fra 1846 til 1849, mens han arbejdede med udformningen af Grundloven. Desuden var han biskop for Lolland-Falster fra 1849 til 1854, men blev afsat efter initiativ fra den konservative statsminister Ørsted, en politisk modstander. Baggrunden var Monrads stadig indsendte petitioner med kritik af regeringen. På dette tidspunkt skal Monrad have været opstemt og uforudsigelig med stadig skiftende meninger.[2] Han blev så tildelt en ledende stilling som skoledirektør.

                        I 1855 blev han departementschef i Kultusministeriet.

                        I forbindelse med forfatningskampen kommenterede D.G. Monrad i 1879 de store sociale skel mellem bondebefolkningen og landbefolkningen således: "Tag en fattig købstadshåndværker, er han end nok så uvidende, kan han end ikke skrive to ord af modersmålet uden i det mindste at begå to fejl, så har han dog en levende følelse af, at han er et ganske andet ophøjet væsen end en velhavende oplyst selvejerbonde".</p>
                    <br>
                    <p>D.G. Monrad indtrådte i martsministeriet i 1848. Han repræsenterede den yngre generation af nationalliberale, der ikke blot engagerede sig i den danske sag i Slesvig, men også ønskede, at et kommende systemskifte skulle medføre sociale reformer.

                        I ministeriet og i forhandlingerne frem til grundloven fra 1849 trådte D.G. Monrad stærkt frem, og havde det stået til ham alene, ville grundloven have fået et yderligere demokratisk præg. Det var Monrad, der skrev det første udkast til Junigrundloven, og han kaldes derfor for grundlovens fader.

                        Som foretagsom kultusminister og indenrigsminister i 1848 og igen 1859-1863 satte Monrad sit præg på folkekirken og undervisningsvæsenet. Han udnævnte Grundtvig til biskop helt uden fuldmagt, til kongens ærgrelse. Ellers huskes han for sine love om anlæg af jernbaner og for sit arbejde med at forbedre fæstebøndernes vilkår.</p>
