            <h1>Personlige liv</h1>

            <p>Orla Lehmann født i København, og var søn af konferensråd Martin Lehmann. Hjemmet var efter faderens ønske tysk, men blandt dets venner var der mange fremragende danske, således både A.S. og H.C. Ørsted, Adam Oehlenschläger og dennes søster Sophie Ørsted, senere også Jens Baggesen. Orla var det ældste af 9 børn og skal have været en overordentlig smuk dreng, moderens yndling, hvis sjældne begavelse allerede tidlig lagde sig for dagen.

                Faderen overtog hans første undervisning, men i sit 9. år sattes han i den nylig oprettede (tyske) Realskole for Petri menighed, til hvilken faderen hørte. En latinklasse blev siden oprettet ved denne, men den viste sig ikke tilfredsstillende, og som 14 års dreng kom Lehmann ind i den københavnske Borgerdydskole under Michael Nielsen, og her fra dimitteredes han 1827 til Københavns Universitet med udmærkelse ligesom hans samtidige Hans Martensen og Frederik Christian Bornemann.

                Efter et vel anvendt rusår, der også benyttedes til litterært studium, således af Goethe, Shakespeare og Heinrich Heine, hvem han læste sammen med H.C. Andersen, begyndte han sit juridiske studium. Fakultetet var den gang dårlig besat, de fleste forelæsninger var åndløse eksegeser og stive systemer, og studiet var ham kun lidet tiltalende, men han tog dog i foråret 1833 sin embedseksamen med bedste karakter.</p>

