            <h1>Professionelle liv</h1>

            <p>Allerede tidlig rørte sig hos ham de to følelser, som blev bestemmende for hele hans liv og virksomhed: Troen på frihedens magt til at fremme alt det ædle og gode i folket og en glødende kærlighed til det danske fædreland. Man kunne sige: til det nordiske fædreland, thi allerede som dreng levede han i skandinaviske drømme -- endnu i den tyske skole skrev han i kammeraternes tyske blad Germania et udkast til en ny Kalmarunion --, og han blev denne tanke tro i gode som i onde tider til sine dages Ende. Hverken hjemmets tyske sprog og dannelse eller skolens tyske undervisning fordunklede hans nationale danske følelse, som var en arv fra moderen, og hans frihedstrang brød igennem selv denne tunge, servile tid, hvis eneste åndelige interesse var teatret og til dels litteraturen, og som han selv så fortræffelig har skildret i sit billede af Frederik 6.s dage.[1]

                Begge fik næring ved Julirevolutionen 1830, hvis bølger gik hen over landene og nåede også det i servil absolutisme nedsunkne Danmark, hvor den rigtignok kun fødte den matte og tamme stænderinstitution. Han måtte føle sig uimodståelig draget til at træde i skranken for sine idéer og bryde igennem den seje skal, som holdt alt offentligt liv nede.</p>
